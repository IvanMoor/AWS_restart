
# Definición de una lista.

myFruitList = ["manzana", "pera", "platano"]
print(myFruitList)
print(type(myFruitList))

print(myFruitList[0])
print(myFruitList[1])
print(myFruitList[2])

# Modificación de los valores de una lista.

myFruitList[2] = "piña"

print(myFruitList)

# Presentar el tipo de dato de tupla

myFinalAnswerTuple = ("melón", "cereza", "fresa")
print(myFinalAnswerTuple)
print(type(myFinalAnswerTuple))

# Acceso a una tupla por posición.

print(myFinalAnswerTuple[0])
print(myFinalAnswerTuple[1])
print(myFinalAnswerTuple[2])

# Presentar el tipo de dato de diccionario.

myFavoriteFruitDictionary = {
  "Sergi" : "platano",
  "JM" : "pera",
  "Andrea" : "manzana"
}

print(myFavoriteFruitDictionary)
print(type(myFavoriteFruitDictionary))

# Acceso al diccionario por nombre.

print(myFavoriteFruitDictionary["Sergi"])
print(myFavoriteFruitDictionary["JM"])
print(myFavoriteFruitDictionary["Andrea"])